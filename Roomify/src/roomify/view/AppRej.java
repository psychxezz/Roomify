    private void setupTable() {
    model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);

    // optional: make rows non-editable
    jTable1.setDefaultEditor(Object.class, null);
}

private void loadPendingListings() {
    model.setRowCount(0);

    for (roomlisting rl : listingController.getPendingListings()) {
        // show only pending
        if (rl.status != null && rl.status.equalsIgnoreCase("Pending")) {
            model.addRow(new Object[]{
                rl.listingId,
                rl.location,
                rl.maxOccupants,
                rl.pricePerWeek,
                rl.status
            });
        }
    }
}

private Integer getSelectedListingId() {
    int row = jTable1.getSelectedRow();
    if (row == -1) return null;
    return Integer.valueOf(model.getValueAt(row, 0).toString()); // listingId in col 0
}
public void reload() {
    loadPendingListings();
}

    


