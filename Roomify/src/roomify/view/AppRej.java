    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
 int row = jTable1.getSelectedRow();

    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Select a listing first.");
        return;
    }

    int listingId = Integer.parseInt(model.getValueAt(row, 0).toString()); 
    // column 0 = listingId

    boolean ok = listingController.declineListing(listingId);

    JOptionPane.showMessageDialog(this, 
        ok ? "Listing rejected!" : "Rejection failed!");

    loadPendingListings();   // refresh table        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed


