   public void refreshRoomsTable() {
    DataStore.seed(); // safe

    viewRooms.clear();
    model.setRowCount(0);

    for (int i = 0; i < DataStore.roomCount; i++) {
        Room r = DataStore.rooms[i];
        if (r != null) {
            viewRooms.add(r);
        }
    }

    // default: show all
    for (Room r : viewRooms) {
        model.addRow(new Object[]{
            r.roomId,
            r.location,
            r.price,
            r.occupants,
            r.availability
        });
    }
}

