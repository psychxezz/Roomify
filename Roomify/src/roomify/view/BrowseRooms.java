    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
int row = jTable1.getSelectedRow();

if (row == -1) {
    javax.swing.JOptionPane.showMessageDialog(this, "Select a room first.");
    return;
}

int roomId = (int) jTable1.getValueAt(row, 0);

// TEMP username (later from session)
String username = currentUsername;

roomify.model.Request r =
    new roomify.model.Request(
        roomify.model.DataStore.nextRequestId++,
        username,
        roomId,
        "Pending"
    );

roomify.model.DataStore.requests.add(r);

javax.swing.JOptionPane.showMessageDialog(this, "Request submitted!");        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
String keyword = jTextField1.getText().trim().toLowerCase();

    model.setRowCount(0);

    for (int i = 0; i < DataStore.roomCount; i++) {
        Room r = DataStore.rooms[i];
        if (r == null) continue;

        boolean match =
                String.valueOf(r.roomId).contains(keyword) ||
                r.location.toLowerCase().contains(keyword) ||
                r.availability.toLowerCase().contains(keyword) ||
                String.valueOf(r.price).contains(keyword) ||
                String.valueOf(r.occupants).contains(keyword);

        if (keyword.isEmpty() || match) {
            model.addRow(new Object[]{
                r.roomId,
                r.location,
                r.price,
                r.occupants,
                r.availability
            });
        }
    }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed


